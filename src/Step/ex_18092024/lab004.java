package Step.ex_18092024;

public class lab004 {
    public static void main(String[] args) {

        // increment (++)//decrement (--)

        //types - pre and post

        // pre - increment - ++operand.
        //incremented first then stored in the result

        int a = 10; //a value 10
        int b = ++a; // a = 1+a value
        System.out.println(b);
        System.out.println(a);

        int c = 20;

        System.out.println(c++); // print first then increment
        System.out.println(c);

    }
}
